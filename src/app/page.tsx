    e.preventDefault();
    try {
      const response = await fetch('/api/emails', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({ email }),
      });

      if (response.ok) {
        setMessage('邮箱添加成功！');
        setEmail('');
      } else {
        setMessage('添加失败，请重试');
      }
    } catch (error) {
      setMessage('发生错误，请稍后重试');
    }
